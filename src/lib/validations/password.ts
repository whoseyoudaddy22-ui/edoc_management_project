import { z } from "zod";

// ใช้ตอนตั้ง/เปลี่ยนรหัสผ่านใหม่เท่านั้น (สร้างผู้ใช้, รีเซ็ตรหัสผ่าน) — ห้ามใช้กับ loginSchema
// เพราะ login ต้องรับรหัสผ่านเดิมของบัญชีที่มีอยู่แล้วเสมอ ไม่ใช่บังคับ policy ใหม่ย้อนหลัง
export const passwordSchema = z
  .string()
  .min(8, "รหัสผ่านต้องมีอย่างน้อย 8 ตัวอักษร")
  .regex(/[A-Z]/, "รหัสผ่านต้องมีตัวอักษรพิมพ์ใหญ่อย่างน้อย 1 ตัว")
  .regex(/[a-z]/, "รหัสผ่านต้องมีตัวอักษรพิมพ์เล็กอย่างน้อย 1 ตัว")
  .regex(/[0-9]/, "รหัสผ่านต้องมีตัวเลขอย่างน้อย 1 ตัว");
